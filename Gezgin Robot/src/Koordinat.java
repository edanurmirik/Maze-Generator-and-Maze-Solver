public class Koordinat {
    int x,y;
    public Koordinat(int x, int y) {
        this.x = x;
        this.y = y;
    }
    public Koordinat(){

    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}
